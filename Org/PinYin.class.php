    /**
     * 将汉字转换为拼音，移除 汉字、数字、英文字母外的字符
     * @param $hanzi string  汉字内容
     * @param string $split  分隔符号，默认空字符串
     * @return string
     */
    public static function hanzi2pinyin($hanzi , $split = '' )
    {
        $data = self::all();
        $ret = "";
        for ($i = 0; $i < mb_strlen($hanzi, 'UTF-8'); $i++) {
            $char = (string)mb_substr($hanzi, $i, 1, 'UTF-8');
            if (isset($data[$char])) {
                $ret .= $data[$char][0] . $split;
            }
            else {
                $char = strtolower($char);
                $ord = ord($char);
                if( ($ord >= 97 && $ord <= 122) || ($ord >= 48 && $ord <= 57) )
                    $ret .= $char;
            }
        }
        //97-122
        return $ret;
    }

